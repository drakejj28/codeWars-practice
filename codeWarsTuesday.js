// The order of the properties in the objects does not matter. 
// The input array will always be valid and formatted as in the example above.

function greetDevelopers(list) {
    // use the map method to go through each developer in the list
    return list.map(dev => ({
      // keep all the original properties of the developer
      ...dev,
      // add a new property 'greeting' with a custom message
      greeting: `Hi ${dev.firstName}, what do you like the most about ${dev.language}?`
    }));
  }

