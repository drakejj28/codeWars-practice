// 1 Complete the function sum such that it returns the sum of a and b.

/**
 * @param {number} a
 * @param {number} b
 */
function sum(a, b) {
    return a + b;
}

// sample usage - do not modify
console.log(sum(1, 3)); // should print 4
console.log(sum(2, 5)); // should print 7

// 2 Complete the function multiply such that it returns the product (the result of multiplication) of a and b.

/**
 * @param {number} a
 * @param {number} b
 */
function multiply(a, b) {
    return a * b;
}

// sample usage - do not modify
console.log(multiply(2, 4)); // 8
console.log(multiply(3, 2)); // 6